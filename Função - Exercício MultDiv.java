class MultDiv {
	public static int multiplicacao(int num1, int num2) {
		return (num1*num2);
	}
	public static int divisao(int num1, int num2) {
		return (num1/num2);
	}
	public static void main (String[]args) {
		System.out.println(MultDiv.multiplicacao(30, 6));
		System.out.println(MultDiv.divisao(1550, 10));
	}
}